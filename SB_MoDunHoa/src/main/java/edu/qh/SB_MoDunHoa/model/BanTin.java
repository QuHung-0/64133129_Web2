package edu.qh.SB_MoDunHoa.model;

public class BanTin {
    private String tieuDe;
    private String noiDung;
    private String anhURL;

    // Getters and Setters
    public String getTieuDe() {
        return tieuDe;
    }
    public void setTieuDe(String tD) {
        this.tieuDe = tD;
    }
    public String getNoiDung() {
        return noiDung;
    }
    public void setNoiDung(String nD) {
        this.noiDung = nD;
    }
    public String getAnhUrl() {
        return anhURL;
    }
    public void setAnhUrl(String aURL) {
        this.anhURL = aURL;
    }
}
